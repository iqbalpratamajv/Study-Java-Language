package LatihanOperator;

public class MenggunakanIncrementdanDecrement {
    public static void main(String[] args){
        int stokAwalToko = 20;
        stokAwalToko++;
        System.out.println("Stok akhir: "+ stokAwalToko);

        stokAwalToko--;
        stokAwalToko--;
        System.out.println("Stok akhir: "+ stokAwalToko);
    }
}
